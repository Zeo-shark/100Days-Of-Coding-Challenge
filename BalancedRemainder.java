package codeforces;

import java.util.*;
public class BalancedRemainder {

	public static void main(String args[])
	{
		Scanner sc= new Scanner(System.in);
		int t= sc.nextInt();
		while(t-->0)
		{
			int n=sc.nextInt();
			int ar[]= new int[n];
			for(int i=0;i<n;i++)
			{
				ar[i]=sc.nextInt();
			}
			
		}
	}
}
